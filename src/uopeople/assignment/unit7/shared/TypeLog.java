 /**
  * Enumeration representing different types of log events.
  * Used to categorize and format logs throughout the application.
  */
 public enum TypeLog {
 
     /** Informational messages. */
     INFO,
 
     /** Messages indicating successful operations. */
     SUCCESS,
 
     /** Warning messages that highlight potential issues. */
     WARNING,
 
     /** Error messages indicating a failure in operations. */
     ERROR
 }
 